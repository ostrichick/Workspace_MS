#include <iostream>  // iostream 헤더 파일을 포함시키는 선행처리기 지시어
using namespace std; // using namespace를 사용해서
// 명칭공간을 생략하고 std:: 없이 cout, endl 등을 사용 가능
int main() // 메인 함수
{
  cout << "나의 첫 번째 C++ 프로그램" << endl; // cout 객체를 이용하여 화면에 텍스트를 콘솔에 출력
  return 0;                                    // 0을 반환하고 종료
}
