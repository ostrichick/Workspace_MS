CREATE TABLE COMMENT_SECTION(
COMMENT_ID INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
COMMENT_CONTENT VARCHAR(1500) NOT NULL,
COMMENT_AUTHOR VARCHAR(30) NOT NULL,
COMMENT_DATE DATETIME DEFAULT NOW()
);

CREATE TABLE ATTACH(
ATTACH_ID INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
ORIGINAL_FILE_NAME VARCHAR(300) NOT NULL,
SYSTEM_FILE_NAME VARCHAR(300) NOT NULL
);

